                              <p>Después del desayuno, conduzca todo el camino de regreso a Mandalay. A su llegada, tome un camino más corto a Amarapura . El Monasterio Mahagandaryone de Amarapura es uno de los monasterios más famosos de todo el país. Aquí puedes experimentar la vida de los monjes. Continúe el viaje a Sagaing Hill con sus aspectos más destacados de Budda Cave, Uminthonze Pagoda, Sunooponnyashin Pagoda.</p>
							<p>Luego cruzamos el río en ferry hasta Inwa (Ava), situada a orillas del río Irrawaddy. Una vez que fue una capital real, Inwa es ahora un tranquilo oasis rural. Disfrute de un paseo en carreta de caballos por el tranquilo campo, visitando Bagaya Kyaung, un hermoso monasterio de madera de teca, Maha aungmyay Bonzán Kyaung y la torre Nan Myint.</p>
							<p>Luego, regrese a Amarapura para el cercano puente de madera U Bein. Al atardecer, camine sobre la madera de teca más larga de su tipo hasta la imagen de Mahamuni , una imagen de bronce de Buda enrocada durante la vida del propio Buda.</p>
