                              <p>Hoy, experimente el tren de la mañana a Nawngpeng a través de Gokteik. Este tren solía ser un infame tren del mercado negro que transportaba mercancías hacia y desde China. Cruce el famoso Viaducto Gokteik de 700 metros de largo , construido en 1900 por la American Pennsylvania Steel Co.</p>
							<p>La impresionante altura desde el puente hasta el fondo del valle es de casi 300 metros. Cruzando el puente, el tren se detiene en una pequeña estación y tendrás la oportunidad de ver mejor el puente. Tenga en cuenta que es posible que no se le permita fotografiar/video ya que los birmanos consideran que el puente es de importancia estratégica.</p>
							<p>Llegada a Kyauk Me por la tarde y paseo por esta encantadora ciudad comercial en medio de las colinas de Shan. Por la tarde, traslado de regreso a su hotel en Pyin oh Lwin.</p>
