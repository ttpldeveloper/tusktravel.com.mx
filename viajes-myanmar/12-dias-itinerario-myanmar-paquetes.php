                              <p>Esta mañana, realice un viaje panorámico de 2 horas cuesta arriba hasta Pyin. oh Lwin, anteriormente Maymyo: una estación de montaña británica ubicada en una meseta a 1070 m sobre el nivel del mar. Visite el colorido mercado local cerca de la torre del reloj Purcell.</p>
							<p>Tome un carruaje tirado por caballos por las calles para ver las casas coloniales británicas únicas de ladrillo y madera. Por la tarde, visite los Jardines Botánicos Nacionales de Kandawgyi: 177 hectáreas con abundancia de flores, bosques y un lago. Dé un relajante paseo por los jardines y disfrute de la belleza de la naturaleza.</p>
							<p>Visite el Hotel Candacraig, construido al estilo de una casa de campo inglesa en 1905. Hay tiempo para un café o té opcional y la oportunidad de tomar fotos de este lugar clásico.</p>
