							<p>Disfrute del almuerzo en un restaurante local, luego cruce Chindwin y conduzca a Powin Taung se detiene en el camino para admirar los pabellones tallados en piedra de Shwe Licenciado en Letras Taung. Conocida como la “Colina de los Alquimistas”, Powin Taung Hill cuenta con varios sitios de cuevas budistas desde el siglo XVI.</p>
							<p>Numerosas imágenes de Buda fueron talladas a lo largo de las paredes de piedra de estas cuevas, creando una galería de diferentes imágenes. A lo largo de los siglos, se agregaron pinturas murales, lo que crea la colección de pintura mural más rica del sudeste asiático. Noche en Monywa.</p>
