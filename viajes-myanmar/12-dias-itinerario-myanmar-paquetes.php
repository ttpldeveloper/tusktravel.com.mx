                           <div class="TourInfo">
                              <p>Mingalabar ! Llegue al aeropuerto internacional de Mandalay, su guía y conductor le darán la bienvenida. Dirígete al muelle de Mandalay y embarca en un barco privado río arriba hasta Mingun. Visite la Pagoda Pahtodawgyi incompleta. Continúe hasta Mingun Bell, campana de 90 toneladas, que es la segunda más grande del mundo y todavía está en buenas condiciones.</p>
							<p>¡No olvides tocarlo! Por la tarde, visite el Palacio de Mandalay, el último palacio real de la última monarquía birmana y el Monasterio de Shwenandaw, la única estructura original importante que queda del Palacio Real original en la actualidad. Continúe hasta la Pagoda Kuthodaw , una estupa budista que contiene el libro más grande del mundo.</p>
							<p>Si el tiempo lo permite, visite el taller de batido de pan de oro. Termine el recorrido visitando Mandalay Hill para ver la puesta de sol y la vista panorámica de la ciudad.</p>
