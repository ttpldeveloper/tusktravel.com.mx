							  <p>También fabrican sombrillas tradicionales hechas con el mismo papel que usan los monjes de todo el país. Luego conduzca hasta el muelle en el lago Inle , el segundo lago más grande de Myanmar, se encuentra a 900 metros sobre el nivel del mar y es el hogar de una técnica única de remo con una sola pierna del pueblo Intha para un bote a motor hasta su hotel, regístrese y relájese.</p>
