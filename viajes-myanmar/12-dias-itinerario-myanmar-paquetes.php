							  <p>Después del almuerzo, el bote lo llevará al hermoso templo de Indein, ubicado en la colina que domina el lago. Es relajante caminar por los pasillos y disfrutar de las columnas gigantes que han sido dejadas a la naturaleza durante siglos.</p>
