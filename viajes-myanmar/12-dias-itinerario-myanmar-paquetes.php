							<p>A su llegada a Pakkoku, visite los talleres de los tejedores de mantas locales (¡ tejidos a mano !) y un mercado especializado en " Thanakha " (un cosmético tradicional birmano hecho con corteza de árbol). Luego, dirígete a Bagan. Regístrese en su hotel y disfrute de su tiempo libre.</p>
